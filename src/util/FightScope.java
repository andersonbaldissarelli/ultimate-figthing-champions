package util;

public class FightScope {
	String idluta;
	String lutador;
	String categoria;
	String defesa;
	String ataque;
	String soco;
	String chute;
	String poder;
	String modalidade;
	
	
	public String getModalidade() {
		return modalidade;
	}
	public void setModalidade(String modalidade) {
		this.modalidade = modalidade;
	}
	public String getLutador() {
		return lutador;
	}
	public void setLutador(String lutador) {
		this.lutador = lutador;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getDefesa() {
		return defesa;
	}
	public void setDefesa(String defesa) {
		this.defesa = defesa;
	}
	public String getAtaque() {
		return ataque;
	}
	public void setAtaque(String ataque) {
		this.ataque = ataque;
	}
	public String getSoco() {
		return soco;
	}
	public void setSoco(String soco) {
		this.soco = soco;
	}
	public String getChute() {
		return chute;
	}
	public void setChute(String chute) {
		this.chute = chute;
	}
	public String getPoder() {
		return poder;
	}
	public void setPoder(String poder) {
		this.poder = poder;
	}
	public String getIdluta() {
		return idluta;
	}
	public void setIdluta(String idluta) {
		this.idluta = idluta;
	}
}
